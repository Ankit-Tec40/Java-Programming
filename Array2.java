
public class Array2 {
    public static void main(String[] args) {
        int [] marks={52,89,45,63,48,36,75};
        System.out.println(marks.length);
        for(int i=0;i<marks.length;i++){
            System.out.println(marks[i]);
        }
        for (int Elemens:marks){
            System.out.println(Elemens);
        }
    }
    
}
